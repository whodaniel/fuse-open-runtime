// Helper function to get agents by category
export function getAgentsByCategory(category: AgentCategory): PredefinedAgent[] {
  return PREDEFINED_AGENTS.filter((agent) => agent.category === category);
}

// Helper function to search agents
export function searchAgents(query: string): PredefinedAgent[] {
  const lowerQuery = query.toLowerCase();
  return PREDEFINED_AGENTS.filter(
    (agent) =>
      agent.name.toLowerCase().includes(lowerQuery) ||
      agent.description.toLowerCase().includes(lowerQuery) ||
      agent.type.toLowerCase().includes(lowerQuery) ||
      agent.capabilities.some((cap) => cap.toLowerCase().includes(lowerQuery))
  );
}

// Get all unique categories
export function getAllCategories(): AgentCategory[] {
  return [...new Set(PREDEFINED_AGENTS.map((agent) => agent.category))];
}

export default PREDEFINED_AGENTS;
