export function getBooksByDDCClass(ddcCode: string): Book[] {
  const prefix = ddcCode.substring(0, 1);
  return SAMPLE_BOOKS.filter(
    (b) => b.ddc?.startsWith(prefix) || b.ddc?.startsWith(ddcCode),
  );
}

export function findBookById(id: string): Book | undefined {
  return SAMPLE_BOOKS.find((b) => b.id === id);
}

export function searchBooks(query: string): Book[] {
  const q = query.toLowerCase();
  return SAMPLE_BOOKS.filter(
    (b) =>
      b.title.toLowerCase().includes(q) ||
      b.author.toLowerCase().includes(q) ||
      b.description?.toLowerCase().includes(q) ||
      b.subjects?.some((s) => s.toLowerCase().includes(q)),
  );
}
